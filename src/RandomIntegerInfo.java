public class RandomIntegerInfo {
	private int previousRandomNumber;
    private int currentRandomNumber;
    private int entry;
    
	public int getPreviousRandomNumber() {
		return previousRandomNumber;
	}
	public void setPreviousRandomNumber(int previousRandomNumber) {
		this.previousRandomNumber = previousRandomNumber;
	}
	public int getCurrentRandomNumber() {
		return currentRandomNumber;
	}
	public void setCurrentRandomNumber(int currentRandomNumber) {
		this.currentRandomNumber = currentRandomNumber;
	}
	public int getEntry() {
		return entry;
	}
	public void setEntry(int entry) {
		this.entry = entry;
	}

}